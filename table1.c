int arctangens(int x,int y)
{
int i;
int	xx,yy;
int  aa,dx,dy,da;
//xx=x*1000;	
//yy=y*1000;	

xx=x;	
yy=y;
	
aa=0;
for (i=0;i<8;i++)
 {

if (xx>=0) {dx=xx >>i;} else { dx=-xx >> i;dx=-dx;}
if (yy>=0) {dy=yy >>i;} else { dy=-yy >> i;dy=-dy;}

  da=atang[i];
  if (yy<0)
          {
          aa=aa-da;
          xx=xx-dy;
          yy=yy+dx;
          }
 else     {
          aa=aa+da;
          xx=xx+dy;
          yy=yy-dx;
          }
 }
return aa/100000;
}
